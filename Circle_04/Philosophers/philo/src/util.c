#include "../philo.h"

int	ft_atoi(char *nbr)
{
	int		neg;
	long	res;

	neg = 1;
	res = 0;
	if (*nbr == '-' || *nbr == '+')
	{
		if (*nbr == '-')
			neg = -1;
		nbr++;
	}
	while (*nbr)
	{
		res = res * 10 + (*nbr - '0');
		nbr++;
	}
	res = res * neg;
	if (res < -2147483648 || res > 2147483647)
		return (-1);
	return (res);
}
