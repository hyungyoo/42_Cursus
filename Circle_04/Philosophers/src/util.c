#include "../philo.h"

int	ft_atoi(char *nbr)
{
	int	neg;
	int	res;

	neg = 1;
	res = 0;
	if (*nbr == '-' || *nbr == '+')
	{
		if (*nbr == '-')
			neg = -1;
		nbr++;
	}
	while (*nbr)
	{
		res = res * 10 + (*nbr - '0');
		nbr++;
	}
	return (res * neg);
}
