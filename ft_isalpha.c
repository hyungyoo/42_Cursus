//
// check if c is an alphabet or not.
// If it's true, it returns 1 or 0.
//
#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c > 64 && c < 91) || (c > 96 && c < 123))
		return (1);
	else
		return (0);
}
