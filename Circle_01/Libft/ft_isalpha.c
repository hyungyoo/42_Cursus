//
// c에 해당하는 아스키코드 값이 알파벳이면 1을, 아니면 0을 반환
//
#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c > 64 && c < 91) || (c > 96 && c < 123))
		return (1);
	else
		return (0);
}
